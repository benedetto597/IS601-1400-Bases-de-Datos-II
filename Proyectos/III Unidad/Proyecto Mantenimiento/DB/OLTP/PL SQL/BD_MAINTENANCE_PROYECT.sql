--------------------------------------------------------
--  DDL para la tabla MATERIAL
--------------------------------------------------------

CREATE TABLE "MATERIALS_PROYECT" (
  "ID_MATERIAL" INT PRIMARY KEY NOT NULL ,
  "COD_MATERIAL" NUMBER(8,0) NOT NULL,
  "UNIT_MEASUREMENT" CHAR(3) DEFAULT 'UN' NOT NULL,
  "AMOUNT" NUMBER NOT NULL
) ;

--------------------------------------------------------
--  DDL para la tabla EQUIPMENT
--------------------------------------------------------

CREATE TABLE "EQUIPMENTS_PROYECT"(
    "ID_EQUIPMENT" INT PRIMARY KEY NOT NULL,
    "COD_EQUIPMENT" CHAR(7) NOT NULL,
    "NAME" VARCHAR2(100) NOT NULL,
    "PRICE" NUMBER(18,2) NOT NULL,
    "CATEGORY" VARCHAR2(500),
    "OPERATIONAL_DATE" DATE NOT NULL,
    "ID_MATERIAL" NUMBER NOT NULL,

CONSTRAINT PK_EQUIPMENT_MATERIAL FOREIGN KEY (ID_MATERIAL) REFERENCES MATERIALS_PROYECT(ID_MATERIAL)
);

--------------------------------------------------------
--  DDL para la tabla EMPLOYEE
--------------------------------------------------------

CREATE TABLE "EMPLOYEES_PROYECT" (
      "ID_EMPLOYEE" INT PRIMARY KEY NOT NULL, 
      "FIRST_NAME" VARCHAR2(20) NOT NULL, 
      "LAST_NAME" VARCHAR2(25) NOT NULL, 
      "PHONE_NUMBER" CHAR(9) DEFAULT '9999-9999' NOT NULL, 
      "HIRE_DATE" DATE NOT NULL, 
      "POSITION" VARCHAR2(30), 
      "SALARY" NUMBER(8,2) NOT NULL, 
      "ID_MANAGER" NUMBER, 
CONSTRAINT PK_EMPLOYEES_MATERIAL FOREIGN KEY (ID_MANAGER) REFERENCES EMPLOYEES_PROYECT(ID_EMPLOYEE)
  ) ;

--------------------------------------------------------
--  DDL para la tabla MAINTENANCE
--------------------------------------------------------

CREATE TABLE "MAINTENANCES_PROYECT" (
      "ID_MAINTENANCE" INT PRIMARY KEY NOT NULL, 
      "TYPE_MAINTENANCE" VARCHAR2(20) NOT NULL, 
      "ACCOUNT" VARCHAR2(20) NOT NULL, 
      "WORKED_HOURS" NUMBER(4,2), 
      "START_DATE" DATE NOT NULL, 
      "END_DATE" DATE, 
      "ID_EMPLOYEE" NUMBER NOT NULL, 
      "ID_EQUIPMENT" NUMBER NOT NULL, 
CONSTRAINT PK_MAINTENANCE_EMPLOYEE FOREIGN KEY (ID_EMPLOYEE) REFERENCES EMPLOYEES_PROYECT(ID_EMPLOYEE),
CONSTRAINT PK_MAINTENANCE_EQUIPMENT FOREIGN KEY (ID_EQUIPMENT) REFERENCES EQUIPMENTS_PROYECT(ID_EQUIPMENT)
  ) ;
  
--------------------------------------------------------
--  DDL para la tabla ORDER
--------------------------------------------------------

CREATE TABLE "ORDERS_PROYECT" (
      "ID_ORDER" INT PRIMARY KEY NOT NULL, 
      "COD_ORDER" CHAR(9) NOT NULL, 
      "TYPE_TRANSACTION" VARCHAR2(20) NOT NULL, 
      "DESC_ORDER" VARCHAR2(500) NOT NULL, 
      "CLASS_ORDER" CHAR(5) NOT NULL, 
      "CLASS_COST" VARCHAR2(100) NOT NULL, 
      "ACCOUNT_DATE" DATE NOT NULL,
      "ID_EMPLOYEE" NUMBER NOT NULL, 
      "ID_MAINTENANCE" NUMBER NOT NULL, 
CONSTRAINT PK_ORDER_EMPLOYEE FOREIGN KEY (ID_EMPLOYEE) REFERENCES EMPLOYEES_PROYECT(ID_EMPLOYEE),
CONSTRAINT PK_ORDER_MAINTENANCE FOREIGN KEY (ID_MAINTENANCE) REFERENCES MAINTENANCES_PROYECT(ID_MAINTENANCE)
  ) ; 
  
--------------------------------------------------------
--  DDL para la tabla ORDER ITEMS
--------------------------------------------------------

CREATE TABLE "ORDERS_ITEMS_PROYECT" (
      "ID_ORDER_ITEMS" INT PRIMARY KEY NOT NULL, 
      "AMOUNT_LEMP" NUMBER(18,2), 
      "AMOUNT_DOLL" NUMBER(18,2), 
      "ADD_AMOUNT" NUMBER NOT NULL, 
      "ID_EQUIPMENT" NUMBER NOT NULL, 
      "ID_MATERIAL" NUMBER NOT NULL, 
      "ID_ORDER" NUMBER NOT NULL, 
CONSTRAINT PK_ORDER_ITEM_EQUIPMENT FOREIGN KEY (ID_EQUIPMENT) REFERENCES EQUIPMENTS_PROYECT(ID_EQUIPMENT),
CONSTRAINT PK_ORDER_ITEM_MATERIAL FOREIGN KEY (ID_MATERIAL) REFERENCES MATERIALS_PROYECT(ID_MATERIAL),
CONSTRAINT PK_ORDER_ITEM_ORDER FOREIGN KEY (ID_ORDER) REFERENCES ORDERS_PROYECT(ID_ORDER)
  ) ; 
  
  
