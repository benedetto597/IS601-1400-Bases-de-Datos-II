CREATE TABLE Capitales (
        id NUMBER,
        nombre VARCHAR2(255) NOT NULL,
        PRIMARY KEY (id)
);

CREATE TABLE Paises (
        id NUMBER,
        nombre VARCHAR2(64) NOT NULL,
        capital NUMBER NOT NULL,
        PRIMARY KEY (id),
        FOREIGN KEY (capital)
            REFERENCES Capitales (id)
);

CREATE TABLE Idiomas (
        id NUMBER,
        nombre VARCHAR2(255) NOT NULL,
        PRIMARY KEY (id)
);

CREATE TABLE IdiomasPais (
        pais NUMBER,
        idioma NUMBER NOT NULL,
        PRIMARY KEY (pais, idioma),
        FOREIGN KEY (pais)
            REFERENCES Paises (id),
        FOREIGN KEY (idioma)
            REFERENCES Idiomas (id)
);

CREATE TABLE Poblaciones (
        pais NUMBER,
        anyo NUMBER(4) NOT NULL,
        poblacion NUMBER,
        PRIMARY KEY (pais, anyo),
        FOREIGN KEY (pais)
            REFERENCES Paises (id)
);


